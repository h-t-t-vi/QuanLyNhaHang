    public void showlist() {
        list = new LoaiThucPhamDAO().readByALL();
        for (LoaiThucPhamDTO dto : list) {
            model.addRow(new Object[]{
                dto.getMaLTP(), dto.getTenLTP()
            });
        }
        tblTP.updateUI();
    }

    public void clearTable() {
        int dong = model.getRowCount();
        for (int i = dong; i > 0; i--) {
            model.removeRow(i - 1);
        }
    }

    public void updateTable() {
        clearTable();
        showlist();
    }

    public void setTextEmpty(String e) {
        txtMaLTP.setText(e);
        txtTenLTP.setText(e);
    }

    public boolean addLTP(LoaiThucPhamDTO dto) {
        if (txtTenLTP.getText().length() == 0 || txtMaLTP.getText().length() == 0) {
            return false;
        }
        dto.setMaLTP(txtMaLTP.getText());
        dto.setTenLTP(txtTenLTP.getText());
        return true;
    }

