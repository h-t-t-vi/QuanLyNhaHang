    private void btcrtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcrtActionPerformed
        LoaiThucPhamDAO dao = new LoaiThucPhamDAO();
        LoaiThucPhamDTO dto = new LoaiThucPhamDTO();
        if (addLTP(dto)) {
            if (new LoaiThucPhamDAO().checkMaLTP(dto)) {
                JOptionPane.showMessageDialog(null, "Mã loại TP đã tồn tại");
                txtMaLTP.requestFocus();
            } else if (JOptionPane.showConfirmDialog(null, "Bạn chắc chắn thêm", "Thông báo", 2) == 0) {
                if (dao.create(dto)) {
                    list = dao.readByALL();
                    int vt = 0;
                    for (int j = 0; j < list.size(); j++) {
                        if (dto.getMaLTP().equals(list.get(j).getMaLTP())) {
                            vt = j;
                        }
                    }
                    updateTable();
                    setTextEmpty("");
                    tblTP.setRowSelectionInterval(vt, vt);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Thêm không thành công");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn không được để trống");
        }
    }//GEN-LAST:event_btcrtActionPerformed

    private void btdlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdlActionPerformed
        LoaiThucPhamDAO dao = new LoaiThucPhamDAO();
        LoaiThucPhamDTO dto = new LoaiThucPhamDTO();
        dto.setMaLTP(txtMaLTP.getText());
        if (JOptionPane.showConfirmDialog(null, "Bạn chắc chắn xóa", "Thông báo", 2) == 0) {
            if (new LoaiThucPhamDAO().checkMaLTP(dto)) {
                if (dao.delete(dto)) {
                    setTextEmpty("");
                } else {
                    JOptionPane.showMessageDialog(null, dao.mess);
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Mã loại thực phẩm không tồn tại");
                txtMaLTP.requestFocus();
                return;
            }
        }
        updateTable();
    }//GEN-LAST:event_btdlActionPerformed

    private void btupdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btupdActionPerformed
        LoaiThucPhamDAO dao = new LoaiThucPhamDAO();
        LoaiThucPhamDTO dto = new LoaiThucPhamDTO();
        if (addLTP(dto)) {
            if (!dao.checkMaLTP(dto)) {
                JOptionPane.showMessageDialog(null, "Mã loại thực phẩm không tồn tại");
                txtMaLTP.requestFocus();
                return;
            }
            if (JOptionPane.showConfirmDialog(null, "Bạn chắc chắn cập nhật lại", "Thông báo", 2) == 0) {
                if (dao.update(dto)) {
                    setTextEmpty("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Cập nhật không thành công");
            }
            updateTable();
            tblTP.setRowSelectionInterval(i, i);
        } else {
            JOptionPane.showMessageDialog(null, "Bạn không được để trống");
        }
    }//GEN-LAST:event_btupdActionPerformed

