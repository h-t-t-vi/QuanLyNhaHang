    public void showList() {
        list = new KhoanPhiDAO().readByAll();
        for (KhoanPhiDTO dto : list) {
            model.addRow(new Object[]{dto.getID(), dto.getTenLoaiHinh(), dto.getLoaiHinh(), dto.getGiaTri(), "" + dto.getNgayBatDau(), "" + dto.getNgayKetThuc()});
        }
        tbAll.updateUI();
    }

    public boolean addKP(KhoanPhiDTO dto) {
        if (txtTenLH.getText().length() == 0 || txtGT.getText().length() == 0 || txtNBD.getDate() == null || txtNKT.getDate() == null) {
            return false;
        }
        try {
            dto.setTenLoaiHinh(txtTenLH.getText());
            TenLH = "" + cbLH.getSelectedItem().toString();
            dto.setLoaiHinh(TenLH);
            dto.setNgayBatDau(txtNBD.getDate());
            dto.setNgayKetThuc(txtNKT.getDate());
            dto.setGiaTri(Integer.parseInt(txtGT.getText()));
        } catch (NumberFormatException e) {
            return false;
        }

        return true;
    }

    public void clearTable() {
        int dong = model.getRowCount();
        for (int j = dong; j > 0; j--) {
            model.removeRow(j - 1);
        }
    }

    public void updateTable() {
        clearTable();
        showList();
    }

