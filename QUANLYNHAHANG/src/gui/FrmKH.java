    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        KhoanPhiDTO dto = new KhoanPhiDTO();
        if (addKP(dto)) {
            KhoanPhiDAO dao = new KhoanPhiDAO();
            if (dao.checkTenLH(dto)) {
                JOptionPane.showMessageDialog(null, "Tên loại hình đã tồn tại");
                txtTenLH.requestFocus();
            } else {
                if (!dao.checkDay(dto.getNgayBatDau(), dto.getNgayKetThuc(), dto.getLoaiHinh())) {
                    JOptionPane.showMessageDialog(this, "Bạn đã nhập trùng thời gian");
                } else if (JOptionPane.showConfirmDialog(null, "Bạn chắc chắn thêm", "Thông báo", 2) == 0) {
                    if (dao.create(dto)) {
                        updateTable();
                        int vt=0;
                        ArrayList<KhoanPhiDTO> list1 = dao.readByAll();
                        for (int j = 0; j < list1.size(); j++) {
                            if (dto.getTenLoaiHinh().equals(this.list.get(j).getTenLoaiHinh())) {
                                vt = j;
                            }
                        }
                        tbAll.setRowSelectionInterval(vt,vt);
                        setTextEmpty("");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Thêm không thành công");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn đã để trống ô hoặc nhập sai giá trị");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        KhoanPhiDTO dto = new KhoanPhiDTO();
        if (addKP(dto)) {
            dto.setID(Integer.parseInt(txtID.getText()));
            KhoanPhiDAO dao = new KhoanPhiDAO();
            if (JOptionPane.showConfirmDialog(null, "Bạn chắc chắn cập nhật lại", "Thông báo", 2) == 0) {
                if (dao.update(dto)) {
                    setTextEmpty("");
                    updateTable();
                    tbAll.setRowSelectionInterval(i, i);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Cập nhật không thành công");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Bạn đã để trống ô hoặc nhập sai giá trị");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

