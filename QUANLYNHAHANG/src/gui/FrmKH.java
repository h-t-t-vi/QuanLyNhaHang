    private void tbAllMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAllMouseClicked
        try {
            i = tbAll.getSelectedRow();
            txtID.setText("" + tbAll.getValueAt(i, 0));
            txtTenLH.setText("" + tbAll.getValueAt(i, 1));
            txtGT.setText("" + tbAll.getValueAt(i, 3));
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String s = "" + tbAll.getValueAt(i, 4);
            String s1 = "" + tbAll.getValueAt(i, 5);
            Date date1 = df.parse(s);
            Date date = df.parse(s1);
            txtNBD.setDate(date1);
            txtNKT.setDate(date);
            ArrayList<String> list1 = new KhoanPhiDAO().readLoaiHinhKP();
            for (int j = 0; j < list1.size(); j++) {
                String row = "" + tbAll.getValueAt(i, 2);
                if (list1.get(j).equals(row)) {
                    cbLH.setSelectedIndex(j);
                }
            }
        } catch (ParseException ex) {
            Logger.getLogger(FrmKH.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbAllMouseClicked

