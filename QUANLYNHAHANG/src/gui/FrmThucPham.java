    private void btdlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdlActionPerformed
        ThucPhamDAO dao = new ThucPhamDAO();
        ThucPhamDTO dto = new ThucPhamDTO();
        dto.setMaTP(txtMaTP.getText());
        dto.setMaLTP(LTP);
        if (JOptionPane.showConfirmDialog(null, "bạn chắc chắn xóa", "Thông báo", 2) == 0) {
            if (dao.delete(dto)) {
                setTextEmpty("");
                String MaLTP = "" + cbMLTP.getSelectedItem().toString();
                cbLTP1.setSelectedItem("" + MaLTP);
                list = new ThucPhamDAO().readAllName(MaLTP);
                updateTable();
            } else {
                JOptionPane.showMessageDialog(null, "Mã TP không tồn tại");
                txtMaTP.requestFocus();
                return;
            }
        }
    }//GEN-LAST:event_btdlActionPerformed

