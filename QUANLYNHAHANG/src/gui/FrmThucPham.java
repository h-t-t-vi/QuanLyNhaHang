    public void settbTP(ArrayList<ThucPhamDTO> list) {
        for (ThucPhamDTO dto : list) {
            model.addRow(new Object[]{
                dto.getTenTP(), dto.getDonvt(), (int) dto.getGia()});
        }
        tblTP.updateUI();
    }

    public boolean addTP(ThucPhamDTO dto) {
        if (txtMaTP.getText().length() == 0 || txtTenTP.getText().length() == 0 || txtGia.getText().length() == 0) {
            return false;
        }
        try {
            dto.setGia(Integer.parseInt(txtGia.getText()));
        } catch (NumberFormatException e) {
            return false;
        }
        dto.setMaTP(txtMaTP.getText());
        dto.setTenTP(txtTenTP.getText());
        String TenLTP = new String(cbMLTP.getSelectedItem().toString());
        String MaLTP = new LoaiThucPhamDAO().readMaLTP(TenLTP);
        dto.setMaLTP(MaLTP);
        String Donvt = "" + cbDvt.getSelectedItem();
        dto.setDonvt(Donvt);
        return true;
    }

    public void showList() {
        for (ThucPhamDTO dto : list) {
            model.addRow(new Object[]{
                dto.getMaTP(), dto.getTenTP(), dto.getMaLTP(), (int) dto.getGia(), dto.getDonvt()});
        }
        tblTP.updateUI();
    }

    public void clearTable() {
        int dong = model.getRowCount();
        for (int a = dong; a > 0; a--) {
            model.removeRow(a - 1);
        }
    }

    public void updateTable() {
        clearTable();
        showList();
    }

    public void setTextEmpty(String e) {
        cbDvt.setSelectedIndex(0);
        txtGia.setText(e);
        txtMaTP.setText(e);
        txtTenTP.setText(e);
    }

